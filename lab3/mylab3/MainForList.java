import List.List;

public class MainForList {
	public static void main(String[] args) {
		List l = new List();
		l.addAtHead(1);
		l.addAtHead(2);
		l.addAtTail(3);
		l.addAtHead(4);
		l.addAtTail(5);
		l.addAtHead(6);		
		System.out.println(l);
		
		System.out.println("Deleted " + l.deleteHead());
		System.out.println(l);
		
		System.out.println("Deleted " + l.deleteTail());
		System.out.println(l);
		
		System.out.println("Deleted " + l.deleteAt(1));
		System.out.println(l);
	}
	
}
